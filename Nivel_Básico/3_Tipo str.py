"""
Pyton = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro das aspas
"""
# Aspas simples 
print('Daniel Fernandes')

# Aspas duplhas
print("Daniel Fernandes")

# Escape 
print("Daniel \"Fernandes\"")

# r
print(r"Daniel \"Fernandes\"")

# Truque para tornar o código mais limpo

print ('Daniel "Fernandes"')