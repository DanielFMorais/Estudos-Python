'''
Tipo de dado boll (Boolean)
Ao questionar algo em um programa,  só existem duas respostas possíveis: 
Sim (true) ou Não (false).
Existem vários operadores para "questionar".  Dentre eles
o ==, que é um operador lógico que questiona se um valor é igual a outro.
'''

print(10 == 10) # sim => true (verdadeiro)
print(10 == 11) # Não => false (falso)
print(type(10 == 11))
print(type(True))
print(type(False))
